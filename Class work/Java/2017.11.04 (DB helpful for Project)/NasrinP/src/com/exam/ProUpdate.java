    public void ddLoad() {
        ddPid.removeAllItems();
        DatabaseCURD dc = new DatabaseCURD();
        ArrayList<Product> al = dc.findAllProduct();
        for (Product product : al) {
            ddPid.addItem(String.valueOf(product.getPid()));
        }
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
//        DatabaseCURD dc = new DatabaseCURD();
//        ArrayList<Product> a1 = dc.findAllProduct();
//        for (Product product : a1) {
//            System.out.println(product.getPid() + " " + product.getPname() + " " + product.getUprice() + " " + product.getStock() + " " + product.getOrigin());
//        }
        String pid = ddPid.getSelectedItem().toString();
        String pname = txtPname.getText();
        String uprice = txtUprice.getText();
        String stock = txtStock.getText();
        String origin = "";
        if (rb1.isSelected()) {
            origin = "Local";
        } else {
            origin = "Origin";
        }
        
        Product p = new Product(Integer.parseInt(pid), pname, Double.parseDouble(uprice), Integer.parseInt(stock), origin);
        DatabaseCURD dc = new DatabaseCURD();
        boolean b = dc.doUpdate(p);
        String a="";
        if (b) {
            a="updated";
            tfDisplay.setText(a);
        } else {
            a="not updated";
            tfDisplay.setText(a);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void ddPidItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ddPidItemStateChanged
        DatabaseCURD dc = new DatabaseCURD();
        int d = Integer.parseInt(ddPid.getSelectedItem().toString());
        Product p = dc.findProductById(d);
        txtPname.setText(p.getPname());
        txtUprice.setText(String.valueOf(p.getUprice()));
        txtStock.setText(String.valueOf(p.getStock()));
        if (p.getOrigin().equals("Local")) {
            rb1.setSelected(true);
        } else {
            rb2.setSelected(true);
        }

    }//GEN-LAST:event_ddPidItemStateChanged

