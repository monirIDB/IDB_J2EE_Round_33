    private void bntCAdd2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntCAdd2ActionPerformed
        // TODO add your handling code here:

        int num = JOptionPane.showConfirmDialog(null, "comfirm delete");
        if (num == JOptionPane.YES_OPTION) {

            if (comDeleteCustomer.getSelectedIndex() > 0) {

                try {
                    int id = Integer.parseInt(comDeleteCustomer.getSelectedItem().toString());
                    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + schema, user, conPass);
                    pst = con.prepareStatement("delete from cleint_table where c_id=?");
                    pst.setInt(1, id);
                   int pNum= pst.executeUpdate();
                    CleintIdInit();
                } catch (SQLException ex) {
                    Logger.getLogger(InventoryManagement.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }


    }//GEN-LAST:event_bntCAdd2ActionPerformed

