    public void saleCalculation() {
        double unitPrice = Double.parseDouble(fldSUnitPrice.getText());
        int quantity = Integer.parseInt(fldSQuatity.getText());
        DecimalFormat decFormat = new DecimalFormat("##.00");

        double grossPrice = unitPrice * quantity;
        double discount = grossPrice * .05;
        double netPrice = grossPrice - discount;

        fldSGrossPrice.setText(decFormat.format(grossPrice));
        fldSDiscount.setText(decFormat.format(discount));
        fldSNetPrice.setText(decFormat.format(netPrice));

    }

