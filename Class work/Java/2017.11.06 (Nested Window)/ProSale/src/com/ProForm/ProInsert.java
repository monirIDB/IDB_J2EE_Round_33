    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String id = tfId.getText();
        String name = tfName.getText();
        String price = tfPrice.getText();
        String stock = tfStock.getText();
        String origin = "";
        if (rb1.isSelected()) {
            origin = "Local";
        } else {
            origin = "Foreign";
        }
        
        
        Product pro = new Product(Integer.parseInt(id), name, Float.parseFloat(price), Integer.parseInt(stock), origin);

        AllMethod am=new AllMethod();
        boolean boo = am.doSave(pro);
        if (boo) {
            String a="Data Inserted";
            tfNotice.setText(a);
        } else {
           String a="Data Inserted";
           tfNotice.setText(a);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

